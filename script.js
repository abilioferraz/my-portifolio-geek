function renderContacts() {
  const contactList = document.querySelector("#contact-list");
  const ctaActions = document.querySelector("#cta-actions");

  if (contactList) {
    const fragment = document.createDocumentFragment();

    portfolioConfig.contacts.forEach((item) => {
      const article = createElement("article", "contact-card");
      const tag = createElement("span", "contact-card__tag", item.tag);
      const title = createElement("h3", "", item.title);
      const description = createElement("p", "", item.description);

      article.append(tag, title, description);

      if (item.href) {
        const footer = createElement("div", "contact-card__footer");
        const link = createElement("a", "contact-link", item.linkLabel || "Abrir");
        link.href = item.href;
        link.target = "_blank";
        link.rel = "noreferrer";
        footer.append(link);
        article.append(footer);
      }

      fragment.append(article);
    });

    contactList.replaceChildren(fragment);
  }

  if (ctaActions) {
    const fragment = document.createDocumentFragment();
    portfolioConfig.actions.cta.forEach((action) => fragment.append(createActionButton(action)));
    ctaActions.replaceChildren(fragment);
  }
}

function setupReveal() {
  const elements = document.querySelectorAll("[data-reveal]");
  const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

  if (prefersReducedMotion.matches || !("IntersectionObserver" in window)) {
    elements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.15,
      rootMargin: "0px 0px -32px 0px"
    }
  );

  elements.forEach((element) => observer.observe(element));
}

function setCurrentYear() {
  setText("#current-year", new Date().getFullYear().toString());
}

function initializePortfolio() {
  renderHero();
  renderTimeline();
  renderProjects();
  renderStack();
  renderQuestLog();
  renderValueProps();
  renderContacts();
  setCurrentYear();
  setupReveal();
}

document.addEventListener("DOMContentLoaded", initializePortfolio);
