function createElement(tagName, className, text) {
  try {
    const element = document.createElement(tagName);

    if (className) {
      element.className = className;
    }

    if (text) {
      element.textContent = text;
    }

    return element;
  } catch (error) {
    console.error(`Error creating element: ${tagName}`, error);
    return null;
  }
}

function setText(selector, value) {
  const element = document.querySelector(selector);

  if (element) {
    element.textContent = value;
  }
}

function createActionButton(item) {
  const action = createElement("a", `button button--${item.variant}`, item.label);
  action.href = item.href;

  if (item.external) {
    action.target = "_blank";
    action.rel = "noreferrer";
  }

  return action;
}

function createTagList(tags) {
  const list = createElement("ul", "tag-list");

  tags.forEach((tag) => {
    list.append(createElement("li", "", tag));
  });

  return list;
}

